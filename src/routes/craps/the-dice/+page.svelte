<h1 class="text-2xl">Shooting the Dice</h1>
<p>
	Getting to throw the dice is one of the best parts of Craps. There's something special about
	having the fate of the entire table at your fingertips. Time almost slows down while the dice are
	in the air, and when you win, it's a feeling hard to match.
</p>

<p>
	Every Craps table has five dice that are considered sacred. It's the dealers' responsibility to
	know where all of them are, so you want to make sure to listen to the rules the dealers tell you
	to follow and don't' do anything crazy. Treat the dice like they're gonna buy you a new pair of
	shoes.
</p>
